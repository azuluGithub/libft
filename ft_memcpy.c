#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	size_t	i;
	char	*d;
	char	*s;

	i = 0;
	d = (char *)s1;
	s = (char *)s2;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (d);
}
