#include "libft.h"

char	*ft_strnew(size_t size)
{
	char		*s;
	size_t		i;

	s = (char *)malloc(size + 1);
	i = 0;
	if (s == NULL)
		return (NULL);
	ft_bzero(s, size + 1);
	return (s);
}
